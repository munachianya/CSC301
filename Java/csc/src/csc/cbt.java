    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int lastQuestion = questions.length;
        boolean unansweredFound = false;
        StringBuilder unansweredQuestionsMessage = new StringBuilder();

        if (i == lastQuestion - 1) {
            // Check for unanswered questions and collect the indices._
            for (int a = 0; a < questions.length; a++) {
                if (YourAnswer[a] == Answer[a]) {
                    ++CorrectAnswer;
                } else if (YourAnswer[a].isEmpty()) {
                    unansweredFound = true;
                    unansweredQuestionsMessage.append("Question " + a + 1 + " :").append("\n");
                }
            }
            if (unansweredFound) {
                JOptionPane.showMessageDialog(rootPane, "You didn't answer the following questions:\n" + unansweredQuestionsMessage.toString());
            }
            JOptionPane.showMessageDialog(rootPane, "This is the last question \n You scored " + CorrectAnswer + " out of " + questions.length);
            jButton3.setEnabled(false);
        } else {
            ++i;
            jTextArea1.setText(questions[i]);
            jRadioButton1.setText(optionA[i]);
            jRadioButton2.setText(optionB[i]);
            jRadioButton3.setText(optionC[i]);
            jRadioButton4.setText(optionD[i]);
            jLabel2.setText("Question" + i);
            buttonGroup1.clearSelection();

            jButton2.setEnabled(true);

        }
        // TODO add your handling code here:__
    }//GEN-LAST:event_jButton3ActionPerformed

